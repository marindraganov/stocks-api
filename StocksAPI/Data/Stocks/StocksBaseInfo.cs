        private readonly Dictionary<int, BaseStockInfo> _stocksByID;
        private readonly Dictionary<string, BaseStockInfo> _stocksBySymbol;


        public StocksBaseInfo()
        {
            _stocksByID = new Dictionary<int, BaseStockInfo>();
            _stocksBySymbol = new Dictionary<string, BaseStockInfo>();

            foreach (var st in stocks)
            {
                var id = int.Parse(st.Split("--")[0]);
                var symbol = st.Split("--")[1];
                var name = st.Split("--")[2];
                var sharesCount = long.Parse(st.Split("--")[3]);

                var stockInfo = new BaseStockInfo(id, symbol, sharesCount, name);

                _stocksByID[id] = stockInfo;
                _stocksBySymbol[symbol] = stockInfo;
            }
        }

        public string GetSymbol(int id)
        {
            return _stocksByID[id].Symbol;
        }

        public int GetID(string symbol)
        {
            return _stocksBySymbol[symbol].ID;
        }

        public BaseStockInfo GetStock(string symbol)
        {
            return _stocksBySymbol[symbol];
        }

        public IEnumerable<BaseStockInfo> GetAllStocks()
        {
            return _stocksByID.Values;
        }

        public record BaseStockInfo(int ID, string Symbol, long SharesCount, string Name);
